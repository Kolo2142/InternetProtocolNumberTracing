package com.meli.tracing.domain.json;

public class IPCountryInfoJsonEntity {
	
	private String countryCode;
	
	private String countryCode3;
	
	private String countryName;
	
	private String countryEmoji;

	/**
	 * @return the countryCode
	 */
	public String getCountryCode() {
		return countryCode;
	}

	/**
	 * @param countryCode the countryCode to set
	 */
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}

	/**
	 * @return the countryCode3
	 */
	public String getCountryCode3() {
		return countryCode3;
	}

	/**
	 * @param countryCode3 the countryCode3 to set
	 */
	public void setCountryCode3(String countryCode3) {
		this.countryCode3 = countryCode3;
	}

	/**
	 * @return the countryName
	 */
	public String getCountryName() {
		return countryName;
	}

	/**
	 * @param countryName the countryName to set
	 */
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}

	/**
	 * @return the countryEmoji
	 */
	public String getCountryEmoji() {
		return countryEmoji;
	}

	/**
	 * @param countryEmoji the countryEmoji to set
	 */
	public void setCountryEmoji(String countryEmoji) {
		this.countryEmoji = countryEmoji;
	}

}
